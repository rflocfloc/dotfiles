vim.opt_local.tabstop = 4         -- Number of spaces a <Tab> counts for.
vim.opt_local.shiftwidth = 4      -- Number of spaces to use for autoindent.
vim.opt_local.spell = false       -- disable spell check for Python code (useful for functions, variables).

-- Set text width for Python. PEP 8 suggests 79 characters, but 80 is common.
vim.opt_local.textwidth = 80      -- Maximum width of text that is wrapped.
vim.opt_local.wrap = true         -- Enable wrapping for long lines.

